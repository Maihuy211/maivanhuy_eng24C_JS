// Sử dụng Math.abs() 
console.log(Math.abs(-5.5)); // Kết quả = 5.5 bỏ dấu âm
console.log(Math.abs(10));   // Kết quả = 10 dữ nguên

// Sử dụng Math.ceil() 
console.log(Math.ceil(4.4)); // Kết quả = 5 lm tròn lên
console.log(Math.ceil(5.8)); // Kết quả = 6 lm trong lên

// Sử dụng Math.floor() 
console.log(Math.floor(5.7)); // Kết quả = 5  làm tròn xuống
console.log(Math.floor(6.1)); // Kết quả = 6 làm tròn xuống

// Sử dụng Math.round() 
console.log(Math.round(10.1)); // Kết quả = 10 lm tròn gần nhất 
console.log(Math.round(20.6)); // Kết quả = 21 lm tròn gần nhất